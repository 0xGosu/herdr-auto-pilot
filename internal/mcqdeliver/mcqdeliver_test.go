// MCQResetKeysForTest mirrors the production reset burst length so the tests
// can assert on the keystrokes that follow it.
const MCQResetKeysForTest = domain.MCQResetKeys

// digitBinding is the per-tab key protocol a rendering uses. Both are real
// Claude Code behaviours, verified live 2026-07-16 against Haiku 4.5.
type digitBinding int

const (
	// digitCommits is the PLAIN rendering: the digit selects the option and
	// auto-advances to the next tab.
	digitCommits digitBinding = iota
	// digitMovesCaret is the PREVIEW rendering: the digit only moves the
	// selection caret (like ↑/↓); Enter is what commits and advances.
	digitMovesCaret
)

// fakeForm simulates a live Claude multi-tab form: N question tabs plus the
// generated Submit tab. It renders the real tab header/footer shapes so the
// production domain parser is what reads it.
type fakeForm struct {
	binding   digitBinding
	questions int
	options   int
	answered  []bool
	current   int // 0-based tab; == questions means the Submit tab
	caret     int // 1-based option under the caret
	submitted bool
	keys      []string
	failKey   string
	// submitBinding overrides binding for the Submit tab. A preview form's
	// Submit tab is generated by Claude and renders plain (audit #671).
	submitBinding digitBinding
	// multiTabs marks question tabs that are MULTI-SELECT: their options carry
	// `[ ]` checkboxes that a digit toggles, and they never auto-advance.
	multiTabs map[int]bool
	// checked[tab][option] is that tab's checkbox state.
	checked map[int]map[int]bool
	// submitStuck models a Submit tab that will not submit, so the fail-closed
	// guard for that state is reachable.
	submitStuck bool
	// toggleDeaf models a multi-select tab that swallows its digits (a build
	// with a different checkbox binding), so the toggle verification is
	// reachable. Without it, a delivery that changes nothing on screen would
	// still advance and submit an empty answer.
	toggleDeaf bool
	// hideAfterToggle drops the trailing options from a multi-select tab once
	// it has been toggled, modelling a re-render whose option list scrolls (or
	// a read window that truncates it). Verification must refuse rather than
	// skip the options it can no longer see.
	hideAfterToggle int
	// stuckTab keeps the advance key from leaving the multi-select tab.
	stuckTab bool
}

// check pre-checks a multi-select tab's option, modelling a pane that already
// carries an earlier delivery attempt's toggles.
func (f *fakeForm) check(tab, option int) {
	if f.checked[tab] == nil {
		f.checked[tab] = map[int]bool{}
	}
	f.checked[tab][option] = true
}

func newFakeForm(binding digitBinding, questions, options int) *fakeForm {
	return &fakeForm{
		binding: binding, questions: questions, options: options,
		answered: make([]bool, questions), caret: 1,
		submitBinding: digitCommits,
		multiTabs:     map[int]bool{},
		checked:       map[int]map[int]bool{},
	}
}

func (f *fakeForm) onSubmitTab() bool { return f.current == f.questions }

func (f *fakeForm) tabBinding() digitBinding {
	if f.onSubmitTab() {
		return f.submitBinding
	}
	return f.binding
}

func (f *fakeForm) SendKey(_ context.Context, _, key string) error {
	if key == f.failKey {
		return errors.New("induced keystroke failure")
	}
	f.keys = append(f.keys, key)
	if f.submitted {
		return nil // keys land in the composer once the form is gone
	}
	switch key {
	case "left":
		if f.current > 0 {
			f.current--
			f.caret = 1
		}
	case "right":
		if f.stuckTab && !f.onSubmitTab() && f.multiTabs[f.current] {
			break // the advance does not leave a multi-select tab
		}
		if f.current < f.questions {
			f.current++
			f.caret = 1
		}
	case "enter":
		f.commit()
	default:
		d, err := strconv.Atoi(key)
		if err != nil || d < 1 || d > f.options {
			break
		}
		// A multi-select tab's digit toggles a checkbox: no caret move, no
		// commit, no advance.
		if !f.onSubmitTab() && f.multiTabs[f.current] {
			if f.toggleDeaf {
				break
			}
			if f.checked[f.current] == nil {
				f.checked[f.current] = map[int]bool{}
			}
			f.checked[f.current][d] = !f.checked[f.current][d]
			break
		}
		f.caret = d
		if f.tabBinding() == digitCommits {
			f.commit()
		}
	}
	return nil
}

// commit applies the current caret selection, mirroring the real form: the
// Submit tab's option 1 submits (the form vanishes), a question tab records its
// answer and auto-advances.
func (f *fakeForm) commit() {
	if f.onSubmitTab() {
		if f.caret == 1 && !f.submitStuck {
			f.submitted = true
		}
		return
	}
	f.answered[f.current] = true
	f.current++
	f.caret = 1
}

func (f *fakeForm) Read(_ context.Context, _ string, _ int) (string, error) {
	if f.submitted {
		return "● The form was submitted. Working on it now.\n\n❯ \n", nil
	}
	var head strings.Builder
	head.WriteString("←  ")
	for i := 0; i < f.questions; i++ {
		mark := "☐"
		if f.answered[i] {
			mark = "☒"
		}
		fmt.Fprintf(&head, "%s Q%d  ", mark, i+1)
	}
	head.WriteString("✔ Submit  →")

	var b strings.Builder
	b.WriteString(head.String() + "\n\n")
	if f.onSubmitTab() {
		b.WriteString("Review your answers\n\nReady to submit your answers?\n\n")
		b.WriteString(f.renderOptions([]string{"Submit answers", "Cancel"}))
		return b.String(), nil
	}
	fmt.Fprintf(&b, "Question %d?\n\n", f.current+1)
	labels := make([]string, f.options)
	for i := range labels {
		if f.multiTabs[f.current] {
			// `[ ]`/`[x]` per option is what marks a tab multi-select on screen
			// (domain.MultiSelectTab / OptionCheckStates).
			box := "[ ]"
			if f.checked[f.current][i+1] {
				box = "[x]"
			}
			labels[i] = fmt.Sprintf("%s Option %d", box, i+1)
			continue
		}
		labels[i] = fmt.Sprintf("Option %d", i+1)
	}
	// A re-render that scrolls (or a truncated read) can show fewer options
	// than the pre-toggle read did.
	if f.hideAfterToggle > 0 && f.multiTabs[f.current] && len(f.checked[f.current]) > 0 &&
		f.hideAfterToggle < len(labels) {
		labels = labels[:f.hideAfterToggle]
	}
	b.WriteString(f.renderOptions(labels))
	b.WriteString("\n" + f.footer())
	return b.String(), nil
}

// footer mirrors the real render: the tab-switch hint appears only when there
// is a sibling question to switch to. A ONE-question form (one tab plus the
// generated Submit tab) shows the same plain footer as a single-question menu,
// which is what made it read as a plain menu and get answered with a blind
// digit that only toggled its checkbox.
func (f *fakeForm) footer() string {
	if f.questions > 1 {
		return "Enter to select · ↑/↓ to navigate · Tab to switch questions · Esc to cancel\n"
	}
	return "Enter to select · ↑/↓ to navigate · Esc to cancel\n"
}

func (f *fakeForm) renderOptions(labels []string) string {
	var b strings.Builder
	for i, label := range labels {
		caret := " "
		if f.caret == i+1 {
			caret = "❯"
		}
		fmt.Fprintf(&b, "%s %d. %s\n", caret, i+1, label)
	}
	return b.String()
}

func (f *fakeForm) config() Config {
	return Config{Keys: f, Read: f.Read, PaneID: "w1:p1", ReadLines: 40, KeyDelay: 0}
}

func groupsOf(digits ...string) [][]string {
	groups := make([][]string, len(digits))
	for i, d := range digits {
		groups[i] = []string{d}
	}
	return groups
}

// The regression this whole package exists for: on a PREVIEW form the digit
// only moves the caret, so digit-only delivery answered nothing and left the
// agent blocked. Both renderings must now land.
func TestClaudeTabsAnswersBothRenderings(t *testing.T) {
	tests := []struct {
		name    string
		binding digitBinding
		answers []string
		// wantKeys is the tail after the reset burst.
		wantKeys []string
	}{
		{
			name:    "plain rendering commits on the digit alone",
			binding: digitCommits,
			answers: []string{"2", "1", "1"},
			// No Enter: each digit already committed and advanced.
			wantKeys: []string{"2", "1", "1"},
		},
		{
			name:    "preview rendering needs Enter to commit each answer",
			binding: digitMovesCaret,
			answers: []string{"2", "1", "1"},
			// Questions need digit+Enter; the generated Submit tab is plain,
			// so its digit commits on its own.
			wantKeys: []string{"2", "enter", "1", "enter", "1"},
		},
	}
	for _, tc := range tests {
		t.Run(tc.name, func(t *testing.T) {
			form := newFakeForm(tc.binding, 2, 3)
			if err := ClaudeTabs(context.Background(), form.config(), groupsOf(tc.answers...), []bool{false, false, false}); err != nil {
				t.Fatalf("ClaudeTabs returned error: %v", err)
			}
			if !form.submitted {
				t.Fatalf("form was not submitted; answered=%v current=%d", form.answered, form.current)
			}
			for i, a := range form.answered {
				if !a {
					t.Errorf("question %d was left unanswered", i+1)
				}
			}
			reset := make([]string, MCQResetKeysForTest)
			for i := range reset {
				reset[i] = "left"
			}
			want := append(reset, tc.wantKeys...)
			if strings.Join(form.keys, ",") != strings.Join(want, ",") {
				t.Errorf("keys sent =\n  %v\nwant\n  %v", form.keys, want)
			}
		})
	}
}

