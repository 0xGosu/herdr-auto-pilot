// Fail closed: if the digit neither commits nor moves the caret to the chosen
// option (e.g. a build unbinds digits entirely), Enter must NOT be pressed —
// it would commit whatever option the caret happens to rest on.
func TestClaudeTabsRefusesWhenCaretDidNotReachOption(t *testing.T) {
	form := newFakeForm(digitMovesCaret, 2, 3)
	form.options = 0 // digits are ignored entirely: caret never moves
	err := ClaudeTabs(context.Background(), form.config(), groupsOf("2", "1", "1"), []bool{false, false, false})
	if err == nil {
		t.Fatal("expected an error when the caret never reached the chosen option")
	}
	if !strings.Contains(err.Error(), "was not selected") {
		t.Errorf("error = %v, want it to name the unselected option", err)
	}
	for _, k := range form.keys {
		if k == "enter" {
			t.Fatal("Enter must not be pressed once the caret is known to be wrong")
		}
	}
	if form.answered[0] {
		t.Error("no answer should have been committed")
	}
}

// A multi-select tab toggles checkboxes (previews and multi-select are mutually
// exclusive, so it is always the plain digit-toggling rendering) and does not
// auto-advance, so it keeps its explicit advance key.
func TestClaudeTabsTogglesAndAdvancesMultiSelectTab(t *testing.T) {
	form := newFakeForm(digitCommits, 1, 3)
	form.multiTabs[0] = true
	err := ClaudeTabs(context.Background(), form.config(),
		[][]string{{"1", "3"}, {"1"}}, []bool{true, false})
	if err != nil {
		t.Fatalf("ClaudeTabs returned error: %v", err)
	}
	tail := form.keys[MCQResetKeysForTest:]
	want := []string{"1", "3", "right", "1"}
	if strings.Join(tail, ",") != strings.Join(want, ",") {
		t.Errorf("keys = %v, want %v", tail, want)
	}
	// The keystrokes must actually have toggled the chosen options — asserting
	// only the key list passes even when every toggle is a no-op.
	if got := form.checked[0]; !got[1] || !got[3] || got[2] {
		t.Errorf("checked = %v, want options 1 and 3 checked and 2 untouched", got)
	}
	if !form.submitted {
		t.Error("the form should still have been submitted after the multi-select tab")
	}
}

// The retry regression (audit #41-#44, 2026-07-20): a digit TOGGLES a checkbox,
// so a second delivery attempt over a pane that already carries the first
// attempt's toggles must not press it again — that would CLEAR the selection
// and submit an empty answer while every keystroke looked delivered. A retry
// has to be idempotent: press nothing, verify, advance.
func TestClaudeTabsDoesNotRetoggleAlreadyChosenOptions(t *testing.T) {
	form := newFakeForm(digitCommits, 1, 3)
	form.multiTabs[0] = true
	form.check(0, 1) // the previous attempt already checked option 1
	form.check(0, 3)

	err := ClaudeTabs(context.Background(), form.config(),
		[][]string{{"1", "3"}, {"1"}}, []bool{true, false})
	if err != nil {
		t.Fatalf("ClaudeTabs returned error: %v", err)
	}
	tail := form.keys[MCQResetKeysForTest:]
	want := []string{"right", "1"} // no toggles: advance, then Submit
	if strings.Join(tail, ",") != strings.Join(want, ",") {
		t.Errorf("keys = %v, want %v (the chosen options must not be re-pressed)", tail, want)
	}
	if got := form.checked[0]; !got[1] || !got[3] {
		t.Errorf("checked = %v, want options 1 and 3 still checked", got)
	}
	if !form.submitted {
		t.Error("the retry should have submitted the form")
	}
}

